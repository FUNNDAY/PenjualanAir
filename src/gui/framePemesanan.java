    private void cariData (String cari) {
        String sqlCari="";
        try {
            if (cari.isEmpty()){
                sqlCari="select*from pemesanan";
            } else {
                sqlCari="select*from pemesanan where ID_PEMESANAN='"+cari+"'"
                        + " or ID_PELANGGAN = '"+cari+"'"
                        + " or ID_PRODUK = '"+cari+"'"
                        + " or TGL_PEMESANAN = '"+cari+"'"
                        + " or JUMLAH_PESAN = '"+cari+"'";
            } 
            crud.settingJudulTabel(tablePemesanan, judulKolom);
            crud.tampilTabel(tablePemesanan, sqlCari, judulKolom);
            crud.settingLebarKolom(tablePemesanan, lebarKolom);
        } catch (Exception e) {
        }
    }
    
