    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (crud.DuplicateKey("pemesanan", "ID_PEMESANAN", txtIdPemesanan.getText()))
            {
                JOptionPane.showMessageDialog(null,"DATA PEMESANAN SUDAH ADA");
            } else {
                String[] isiField={txtIdPemesanan.getText(),txtIdPelanggan.getText(),txtIdProduk.getText(),txtTanggalPemesanan.getText(),txtJumlahPesan.getText()};
                crud.SimpanDinamis("pemesanan", fieldSimpan, isiField);
                JOptionPane.showMessageDialog(null,"DATA BERHASIL DISIMPAN");
            }
         
            refreshPemesanan();
            bersihForm();
                    
        }   catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

