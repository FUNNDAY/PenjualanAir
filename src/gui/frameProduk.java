    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (crud.DuplicateKey("produk", "ID_PRODUK", txtIdProduk.getText()))
            {
                JOptionPane.showMessageDialog(null,"DATA PRODUK SUDAH ADA");
            } else {
                String[] isiField={txtIdProduk.getText(),txtNamaProduk.getText(),txtHargaProduk.getText()};
                crud.SimpanDinamis("produk", fieldSimpan, isiField);
                JOptionPane.showMessageDialog(null,"DATA BERHASIL DISIMPAN");
            }
            
        refreshProduk();
        bersihForm();
            
        }   catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, 
        "Apakah anda yakin ingin menghapus data ini?", 
        "Konfirmasi Hapus", 
        JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            crud.HapusDinamis("produk", "ID_PRODUK", txtIdProduk.getText());
            bersihForm();
        }
        
        refreshProduk();
        bersihForm();
    }//GEN-LAST:event_jButton3ActionPerformed

