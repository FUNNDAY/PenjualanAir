    private void cariData(String cari){
        String sqlCari="";
        try {
            if (cari.isEmpty()){
                sqlCari="select*from produk";
            } else {
                sqlCari="select*from produk where ID_PRODUK='"+cari+"'"
                        + " or NAMA_PRODUK= '"+cari+"'"
                        + " or HARGA_PRODUK= '"+cari+"'";
            }
        
        crud.settingJudulTabel(tableProduk, judulKolom);
        crud.tampilTabel(tableProduk, sqlCari, judulKolom);
        crud.settingLebarKolom(tableProduk, lebarKolom);
        
        } catch (Exception e) {
        }
    }
    
