    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            if (txtLaporan.getText().isEmpty()){
                crud.tampilLaporan("src/laporan/produk.jrxml", "select*from Produk");
            } else {
                String sql="select*from produk where ID_PRODUK='"+txtLaporan.getText()+"'"+
                    " or NAMA_PRODUK='"+txtLaporan.getText()+"'"+
                    " or HARGA_PRODUK='"+txtLaporan.getText()+"'";
                crud.tampilLaporan("src/laporan/produk.jrxml",sql);
            }

        }   catch (Exception ex) {
            Logger.getLogger(frameProduk.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

