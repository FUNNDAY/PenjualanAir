    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (crud.DuplicateKey("penjualan", "ID_PENJUALAN", txtIdPenjualan.getText()))
            {
                JOptionPane.showMessageDialog(null,"DATA PENJUALAN SUDAH ADA");
            } else {
                String[] isiField={txtIdPenjualan.getText(),txtIdPelanggan.getText(),txtIdPemesanan.getText(),txtTotalPenjualan.getText(),txtTanggalPenjualan.getText()};
                crud.SimpanDinamis("penjualan", fieldSimpan, isiField);
                JOptionPane.showMessageDialog(null,"DATA BERHASIL DISIMPAN");
            }
            
            refreshPenjualan();
            bersihForm();
            
        }   catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

