    private void cariData (String cari) {
        String sqlCari="";
        try {
            if (cari.isEmpty()){
                sqlCari="select*from penjualan";
            } else {
                sqlCari="select*from penjualan where ID_PENJUALAN='"+cari+"'"
                        + " or  ID_PELANGGAN = '"+cari+"'"
                        + " or ID_PEMESANAN = '"+cari+"'"
                        + " or GRAND_TOTAL_PENJUALAN = '"+cari+"'"
                        + " or TGL_PENJUALAN = '"+cari+"'";
            } 
            crud.settingJudulTabel(tablePenjualan, judulKolom);
            crud.tampilTabel(tablePenjualan, sqlCari, judulKolom);
            crud.settingLebarKolom(tablePenjualan, lebarKolom);
        } catch (Exception e) {
        }
    }
    
