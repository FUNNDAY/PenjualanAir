    private void tablePenjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePenjualanMouseClicked
        int selectedRow = tablePenjualan.getSelectedRow();
        if (selectedRow != -1) {

            txtIdPenjualan.setText(tablePenjualan.getValueAt(selectedRow, 0).toString());
            txtIdPelanggan.setText(tablePenjualan.getValueAt(selectedRow, 1).toString());
            txtIdPemesanan.setText(tablePenjualan.getValueAt(selectedRow, 2).toString());
            txtTotalPenjualan.setText(tablePenjualan.getValueAt(selectedRow, 3).toString());
            txtTanggalPenjualan.setText(tablePenjualan.getValueAt(selectedRow, 4).toString());        }
    }//GEN-LAST:event_tablePenjualanMouseClicked

